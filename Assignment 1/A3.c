#include <stdio.h>

void main() {
    int n1, n2;

    printf("Relational Operators\n");
    printf("Please enter the first number: ");
    scanf("%d", &n1);
    printf("Please enter the second number: ");
    scanf("%d", &n2);
    printf("n1 > n2: %d\n", n1 > n2);
    printf("n1 < n2: %d\n", n1 < n2);
    printf("n1 == n2: %d\n", n1 == n2);
    printf("n1 != n2: %d\n", n1 != n2);
    printf("n1 >= n2: %d\n", n1 >= n2);
    printf("n1 <= n2: %d\n", n1 <= n2);
}


